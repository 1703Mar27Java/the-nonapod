$(document).ready(function(){
	
	$("button[name = 'register']").click(function(){
		$("#login-div").slideToggle();
		$("#register-div").slideToggle();
	});
	
	$("button[name = 'cancel-register']").click(function(){
		$("#login-div").slideToggle();
		$("#register-div").slideToggle();
	});
	
	
	
});

