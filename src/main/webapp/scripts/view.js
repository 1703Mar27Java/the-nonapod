$(document).ready(function(){
	
	
	$("#like").click(function(){
		$("#like").fadeOut("slow");
		$("#dislike").fadeOut("slow");
		$("#flag").fadeOut("slow");
	});
	
	$("#dislike").click(function(){
		$("#like").fadeOut("slow");
		$("#dislike").fadeOut("slow");
		$("#flag").fadeOut("slow");
	});
	
	$("#flag").click(function(){
		$("#like").fadeOut("slow");
		$("#dislike").fadeOut("slow");
		$("#flag").fadeOut("slow");
	});
	
    
	
	
});